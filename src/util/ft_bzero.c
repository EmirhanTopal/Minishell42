#include "minishell.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*s_uc;
	size_t			i;

	s_uc = s;
	i = 0;
	while (i < n)
	{
		s_uc[i] = 0;
		i++;
	}
}
